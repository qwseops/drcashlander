<p> No, questo è quanto voglio puntualizzare: agisce a livello cellulare,
										ripristinando il corretto flusso sanguigno. Pertanto, cura tutti i tipi di
										disturbi legati alle articolazioni e alla colonna vertebrale: artrosi e artrite,
										sciatica, reumatismi, radicolite, ernia del disco. Lesioni, traumi, contusioni,
										fratture, persino callosi: questo gel è estremamente efficace per il
										trattamento di tutte queste patologie. </p>
<p>
<strong> Pare che lei stia suggerendo trattarsi di un rimedio ad ampio spettro.
											Davvero riesce a curare tutte queste malattie e non allevia semplicemente il
											dolore? </strong>
</p>
<p> Il gel ArtroFlex Active allevia il dolore (nelle prime fasi del trattamento) e
										infine cura completamente la malattia. Non fraintendetemi, mi piacciono gli
										esercizi e lo sport e non ho intenzione di smettere, ma per la maggior parte dei
										miei pazienti questo gel è il metodo di trattamento più semplice, più
										efficace e il migliore disponibile. </p>
<p><strong> Immagino che, a questo punto, molti vorranno sapere dove trovarlo.
										</strong></p>
<p> Inizialmente avremmo voluto distribuirlo alle farmacie, ma non siamo riusciti a
