<p><strong> Filippo De Angelis </strong>
<br> Specialista in scienze mediche, Italia. Fondatore della moderna
											Kinesioterapia (neurologia e ortopedia) - un metodo alternativo per il
											trattamento di malattie croniche e lesioni dell'apparato
											muscolo-scheletrico. Il principio fondante di questo nuovo metodo non è
											quello di assumere farmaci o indossare corsetti: si basa, piuttosto, su una
											migliore comprensione del proprio corpo e del suo potenziale.
											<br> Esperienza: oltre 30 anni </p>
<div style="clear: both;"></div>
</div>
<p>
<i> "Ricordate una semplice verità e non ascoltate nessuno: le malattie
											articolari sono curabili anche in età avanzata" </i>
</p>
<p> Questo specialista afferma di poter curare un'osteocondrosi sviluppatasi
										nell'arco di 18 anni in un paio di mesi. Sostiene addirittura di aver
										curato un'anziana signora affetta da una grave forma di artrite in soli 78
										giorni. Per quanto riguarda i dolori articolari, se trattati correttamente,
										spariranno entro 4 giorni! Nei 48 anni in cui ha esercitato la professione, ha
										confermato ciascuna delle sue affermazioni dimostrandone la validità sul campo.
										Immediatamente dopo la trasmissione in diretta sul canale "Doctor's
										Life" (il programma sull'argomento "come mantenere le
										articolazioni in salute a qualsiasi età") un noto specialista, Filippo De
										Angelis, ha accettato di rilasciarci un'intervista. </p>
<p>
<strong> Salve, Specialista De Angelis. Mi dica, è vero ciò che si dice e cioè
											che i
											disturbi articolari e l'osteocondrosi sono condizioni inevitabili per
											le persone anziane? </strong>
</p>
<p> Salve Martina, Certo che no. Concepire un'eccessiva fiducia per medici che
										tentano di curarli per dieci anni di seguito senza ottenere risultati, questo è
										ciò che mi sembra, francamente, inevitabile per le persone anziane. In realtà,
										le articolazioni, la condrosi e l'intero scheletro possono essere trattati
										molto bene a qualsiasi età. E non si tratta di un miracolo, ma di scienza allo
										stato puro. </p>
<p> Se si conosce il segreto e si fa qualche sforzo in termini di autodisciplina, si
										possono curare le articolazioni a casa in tempi brevi, migliaia di miei pazienti
										lo fanno. </p>
<p><strong> E qual è il segreto? </strong></p>
<p> Il segreto è capire la causa dei dolori. In tutto, sono fino a 147 le possibili
										cause per l'osteocondrosi e l'artrosi indicate nei testi medici, ma la
										conseguenza è sempre la stessa: le articolazioni, le vertebre e la cartilagine
										perdono elasticità, da qui il dolore. Si logorano a causa della cattiva
										circolazione sanguigna.
									</p>
<p> E questo è tutto il segreto: noi curiamo le articolazioni ripristinando un buon
										apporto di sangue verso di esse. </p>
<p>
<strong> Ma non si ritiene generalmente che sia quasi impossibile ripristinare
											la circolazione sanguigna dopo i 45 anni? </strong>
</p>
<p> Questa è una totale assurdità. Mi sono tirato fuori da una sedia a rotelle dopo
										un infortunio, direi che le mie condizioni erano più gravi che "compiere 45
										anni". </p>
<p><strong> Ha salvato altri dalla sedia a rotelle? </strong></p>
<p> Sì, e più di una volta. Ma la maggior parte dei miei pazienti sono persone
										comuni oltre i 40 anni che hanno sviluppato questi disturbi a causa
										dell'età. Vengono da me con problemi spesso simili tra loro: osteocondrosi,
										artrisi, radicolite, nervi compressi. Queste malattie sono sfiancanti e non
										permettono di condurre una vita normale. </p>
<p> La gente si lamenta del dolore, per loro persino camminare è difficile. Sono
										persone comuni, non sportivi o persone affette da disabilità. Si chiedono
										"Perché a me? Cosa ho fatto?" Io rispondo chiaramente "basta
										piagnucolare, iniziamo a ripristinare la circolazione sanguigna".
									</p>
<p><strong> Come si ripristina la circolazione sanguigna a quest'età? </strong>
</p>
<p> Fino a poco tempo fa curavo i miei pazienti con l'aiuto di un complesso
										programma di movimenti composto da 96 esercizi e con il supporto di allenatori.
										Si tratta di un percorso di cura estremamente efficace, ma lungo e difficile.
										<br> Provoca dolore, è duro, la gente non ha il tempo di andare in palestra. Ero
										convinto che esistesse un modo più facile e moderno e l'ho trovato.
									</p>
<p><strong> Davvero interessante! Potrebbe parlarne ai nostri lettori? </strong></p>
<p> Certo. Il nostro centro è stato uno dei primi, in Italia, a ottenere
										l'accesso certificato a un rimedio nuovissimo per il trattamento di
										osteocondrosi e dolori articolari. Devo ammetterlo, quando ne ho sentito parlare
										per la prima volta, ho riso, perché non riuscivo a credere nella sua efficacia.
										Ma sono rimasto sbalordito dai risultati dei nostri test: 4567 pazienti si sono
										completamente sbarazzati dei loro disturbi, vale a dire, più del 94% di tutti i
										soggetti. il 5,6% ha registrato un miglioramento significativo e solo lo 0,4%
										non ha notato alcun cambiamento. </p>
